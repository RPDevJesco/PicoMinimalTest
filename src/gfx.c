/* ---- Character / string drawing -------------------------------------- */

void gfx_draw_char(uint16_t x, uint16_t y, char c,
                   uint16_t fg, uint16_t bg, uint8_t scale)
{
    if (c < 32 || c > 126) c = '?';
    const uint8_t *glyph = &font5x7[(c - 32) * 5];

    for (uint8_t col = 0; col < 5; col++) {
        uint8_t bits = glyph[col];
        for (uint8_t row = 0; row < 7; row++) {
            uint16_t color = (bits & (1 << row)) ? fg : bg;
            if (scale == 1) {
                if (color != bg || bg != 0xFFFF)  /* skip transparent */
                    ili9488_pixel(x + col, y + row, color);
            } else {
                ili9488_fill_rect(x + col * scale, y + row * scale,
                                  x + col * scale + scale - 1,
                                  y + row * scale + scale - 1,
                                  color);
            }
        }
    }
    /* 1-pixel gap between characters */
    if (scale == 1) {
        for (uint8_t row = 0; row < 7; row++)
            if (bg != 0xFFFF) ili9488_pixel(x + 5, y + row, bg);
    } else {
        ili9488_fill_rect(x + 5 * scale, y,
                          x + 6 * scale - 1, y + 7 * scale - 1, bg);
    }
}

uint16_t gfx_draw_string(uint16_t x, uint16_t y, const char *str,
                         uint16_t fg, uint16_t bg, uint8_t scale)
{
    while (*str) {
        gfx_draw_char(x, y, *str++, fg, bg, scale);
        x += 6 * scale;
    }
    return x;
}

uint16_t gfx_string_width(const char *str, uint8_t scale)
{
    return (uint16_t)(strlen(str) * 6 * scale);
}

void gfx_draw_string_centered(uint16_t y, const char *str,
                               uint16_t fg, uint16_t bg, uint8_t scale)
{
    uint16_t w = gfx_string_width(str, scale);
    uint16_t sw = ili9488_width();
    uint16_t x = (sw > w) ? (sw - w) / 2 : 0;
    gfx_draw_string(x, y, str, fg, bg, scale);
}

/* ---- Progress bar ---------------------------------------------------- */

void gfx_progress_bar(uint16_t x, uint16_t y, uint16_t w, uint16_t h,
                      uint8_t pct, uint16_t fg, uint16_t bg, uint16_t border)
{
    if (pct > 100) pct = 100;
    ili9488_rect(x, y, x + w - 1, y + h - 1, border);
    uint16_t inner_w = (uint16_t)((uint32_t)(w - 4) * pct / 100);
    if (inner_w > 0)
        ili9488_fill_rect(x + 2, y + 2, x + 2 + inner_w - 1, y + h - 3, fg);
    if (inner_w < w - 4)
        ili9488_fill_rect(x + 2 + inner_w, y + 2, x + w - 3, y + h - 3, bg);
}

/* ---- Colour utilities ------------------------------------------------ */

uint16_t gfx_color_wheel(uint16_t hue)
{
    hue %= 360;
    uint8_t region = hue / 60;
    uint8_t frac   = (uint8_t)((hue % 60) * 255 / 60);

    uint8_t r, g, b;
    switch (region) {
        case 0:  r = 255; g = frac;       b = 0;           break;
        case 1:  r = 255 - frac; g = 255; b = 0;           break;
        case 2:  r = 0;   g = 255;        b = frac;        break;
        case 3:  r = 0;   g = 255 - frac; b = 255;         break;
        case 4:  r = frac; g = 0;         b = 255;         break;
        default: r = 255; g = 0;          b = 255 - frac;  break;
    }
    return RGB565(r, g, b);
}

uint16_t gfx_lerp_color(uint16_t c1, uint16_t c2, uint8_t t)
{
    uint8_t r1 = (c1 >> 11) & 0x1F, g1 = (c1 >> 5) & 0x3F, b1 = c1 & 0x1F;
    uint8_t r2 = (c2 >> 11) & 0x1F, g2 = (c2 >> 5) & 0x3F, b2 = c2 & 0x1F;
    uint8_t r = r1 + ((int16_t)(r2 - r1) * t / 255);
    uint8_t g = g1 + ((int16_t)(g2 - g1) * t / 255);
    uint8_t b = b1 + ((int16_t)(b2 - b1) * t / 255);
    return (r << 11) | (g << 5) | b;
}

void gfx_gradient_h(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1,
                    uint16_t c1, uint16_t c2)
{
    uint16_t w = x1 - x0 + 1;
    for (uint16_t x = 0; x < w; x++) {
        uint8_t t = (uint8_t)((uint32_t)x * 255 / (w - 1));
        uint16_t c = gfx_lerp_color(c1, c2, t);
        ili9488_vline(x0 + x, y0, y1 - y0 + 1, c);
    }
}

void gfx_gradient_v(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1,
                    uint16_t c1, uint16_t c2)
{
    uint16_t h = y1 - y0 + 1;
    for (uint16_t y = 0; y < h; y++) {
        uint8_t t = (uint8_t)((uint32_t)y * 255 / (h - 1));
        uint16_t c = gfx_lerp_color(c1, c2, t);
        ili9488_hline(x0, y0 + y, x1 - x0 + 1, c);
    }
}
